// Using let
let city = "Mandaluyong"
city = "Quezon" // Reassignment is allowed
console.log(city)

// Using const
const country = "Philippines"
country = "South Korea" // Reassignment is not allowed
console.log(country)

// Using var
var myName = "Eunice"
var myAge = 20
myAge = 21 // Reassignment is allowed
console.log(myName, myAge)